  </h2>
  <p class="first-paragraph">
    A.T.T.V. ’71 is een afkorting voor, het laat zich al raden: Astense Tafeltennisvereniging. 
    Deze vereniging is opgericht in 1971 en heeft circa 35 actieve leden. Bij ATTV’71 kun je tafeltennissen, 
    dit is zowel mogelijk met leden van de club als met leden van andere clubs. 
    Dit kan op onze trainingsavonden op donderdagavond of door deel te nemen aan toernooien of de NTTB-competitie.
  </p>
  <p>
    De trainingsavond is op donderdag in sporthal “de Schop”, Lienderweg 66 in Asten.
    We trainen op alle donderdagen die niet in de zomervakantie dan wel op een nationale feestdag vallen.
    De jeugd (tot en met 16 jaar) traint van 19.00 - 21.00 uur
    Van 21.00 - 23.00 uur is er voor senioren de gelegenheid een balletje te slaan.
    Gedurende de zomervakantie in de maanden juli en augustus zijn er géén trainingsavonden.
    Heb je interesse, kom dan eens meedoen. Je kunt drie keer vrijblijvend meetrainen op onze train
  </p>
  <p>
    Heb je interesse, kom dan eens meedoen. 
    Je kunt drie keer vrijblijvend meetrainen op onze trainingsavonden en beslis daarna pas of dit nu de sport is die je altijd al in clubverband had willen doen.
  </p>
  <p>
    Als je wilt kun je bij onze vereniging deelnemen aan de competitie van de Nederlandse Tafeltennis bond. 
    Er is een voorjaarscompetitie, deze loopt van januari tot mei en een najaarscompetitie, van september tot december. 
    De jeugd competitiewedstrijden worden gespeeld op zaterdag. 
    De senioren competitiewedstrijden worden thuis op donderdagavond gespeeld en uit op de trainingsavond/wedstrijdavond van de te bezoeken club.
  </p>
  <p>
    ATTV’71 organiseert verschillende activiteiten voor haar leden, hierbij valt te denken aan de clubkampioenschappen, 
    welke jaarlijks in minitoernooi plaatsvinden bij zowel de jeugd als senioren. 
    Daarnaast organiseren we jaarlijks een groot toernooi, de Open Astense Tafeltenniskampioenschappen, toegankelijk voor iedere tafeltennisliefhebber. 
    Ook nemen we regelmatig deel aan toernooien in de buurt. 
    Voor een compleet overzicht kunt u op de ATTV-site kijken naar de activiteitenkalender van ATTV’71.
  </p>
  <p>
    De contributie per halfjaar bedraagt:
  </p>
  <ul>
    <li>€ 33,- voor jeugdleden tot en met 12 jaar</li>
    <li>€ 39,- voor jeugdleden tot en met 16 jaar</li>
    <li>€ 60,- voor seniorleden</li>
  </ul>
  <p>
    Daarbij kost deelname aan de (NTTB-)competitie per halfjaar € 7,- voor jeugd- en € 12,- voor seniorenleden.
    De contributie wordt via automatische incasso per halfjaar geheven.
  </p>
  <p>
    Verder vinden wij het belangrijk dat onze leden bij wedstrijden, toernooien en clubkampioenschappen herkenbaar zijn als lid van ATTV ’71 door middel van het clubshirt. 
    Dit clubshirt kost € 35,- en is te verkrijgen via de vereniging (bestuur). Men dient hierbij zelf voor een bijpassende zwarte broek te zorgen.
  </p>
