  <div class="text-lg">
    <p>
      De NTTB organiseert tweemaal per jaar de afdelingscompetitie ZuidWest, waar ATTV'71 aan deelneemt. 
      Deze competitie is van januari t/m april en van september t/m november. 
      Ieder seizoen bestaat normaal gesproken uit 10 wedstrijddagen, waarbij ieder team 3 spelers meeneemt. 
      Iedere speler van het ene team speelt tegen iedere speler van het andere team, in totaal 9 wedstrijden. 
      Er wordt ook eenmaal een dubbelspel gespeeld, waarbij twee spelers van het ene team het opnemen tegen twee spelers uit het andere team. 
      In totaal worden er dus 10 wedstrijden gespeeld op een wedstrijddag.
    </p>
